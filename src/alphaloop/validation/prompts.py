def build_scoring_review_system_prompt(
    asset: AssetConfig,
) -> str:
    """System prompt for AI review of scoring engine decisions."""
    return f"""You are a senior {asset.display_name} ({asset.symbol}) risk manager reviewing a SCORED trade signal.

A probabilistic scoring engine has already analyzed {asset.asset_class} market data through 22 technical plugins grouped into 5 categories (trend, momentum, structure, volume, volatility). Each group outputs a 0-100 score. The groups are combined via weighted average into a total confidence score.

Your job is to REVIEW the scoring engine's output and decide whether to APPROVE, ADJUST, or VETO the trade.

You add value where indicators cannot:
- Cross-asset regime awareness (is the broader market environment hostile?)
- News/macro context that hasn't hit price yet
- Pattern quality judgment (is this a textbook setup or a marginal one?)
- Risk concentration (correlated positions, drawdown context)

You MUST respond with ONLY valid JSON:
{{
  "verdict": "APPROVE" or "ADJUST" or "VETO",
  "adjusted_confidence": null or 0-100 (only if verdict is ADJUST),
  "veto_reasons": ["reason1"] or [],
  "risk_score": 0.0-1.0 (0=safest, 1=highest risk),
  "reasoning": "Your concise assessment (2-3 sentences max)"
}}

Rules:
- APPROVE: scoring engine's decision stands as-is
- ADJUST: you agree with direction but want to modify confidence (affects position size)
- VETO: block the trade entirely (use sparingly — only for risks indicators cannot see)
- Do NOT second-guess indicator math. Your edge is context, not recalculating RSI.
- When in doubt between APPROVE and ADJUST, prefer ADJUST over VETO.
- Protecting capital is paramount. But excessive vetoing destroys the system's edge."""


def build_scoring_review_prompt(
    signal,
    context: dict,
    decision,
    feature_results: list,
    asset: AssetConfig | None = None,
) -> str:
    """
    Build the scoring review prompt with full scoring context.

    Args:
        signal: TradeSignal from algorithmic engine
        context: MarketContext dict
        decision: ScoringDecision from scoring engine
        feature_results: list[FeatureResult] from feature pipeline
        asset: AssetConfig for the symbol
    """
    asset_label = asset.display_name if asset else "Asset"

    # Group scores breakdown
    group_lines = []
    for group, score in sorted(decision.group_scores.items()):
        group_lines.append(f"  {group:12s}: {score:.1f}/100")
    group_text = "\n".join(group_lines) if group_lines else "  (no group scores)"

    # Per-plugin feature breakdown
    feature_lines = []
    for fr in feature_results:
        feats = " ".join(f"{k}={v:.1f}" for k, v in fr.features.items())
        feature_lines.append(f"  [{fr.group:10s}] {fr.tool_name:22s}: {feats}")
    feature_text = "\n".join(feature_lines) if feature_lines else "  (no features)"

    # Market context
    h1_ind = context.get("timeframes", {}).get("H1", {}).get("indicators", {})
    m15_ind = context.get("timeframes", {}).get("M15", {}).get("indicators", {})

    news_events = context.get("upcoming_news", [])
    news_text = "  None in window"
    if news_events:
        lines = []
        for ev in news_events:
            lines.append(f"  [{ev.get('impact', '?')}] {ev.get('time')} — {ev.get('name')}")
        news_text = "\n".join(lines)

    return f"""=== {asset_label.upper()} SCORING ENGINE REVIEW ===

DECISION:
  Direction: {decision.direction}
  Confidence: {decision.raw_confidence:.1f}/100
  Size Scalar: {decision.size_scalar:.2f}x
  Engine Reasoning: {decision.reasoning}

SIGNAL (from algorithmic engine):
  Direction: {signal.direction}
  Setup: {signal.setup.value}
  Entry Zone: {signal.entry_zone[0]} – {signal.entry_zone[1]}
  Stop Loss: {signal.stop_loss}
  Take Profit: {signal.take_profit}
  R:R to TP1: {signal.rr_ratio_tp1}

GROUP SCORES:
{group_text}

PLUGIN FEATURES (raw):
{feature_text}

H1 CONTEXT:
  EMA_FAST: {h1_ind.get('ema_fast')} | EMA_SLOW: {h1_ind.get('ema_slow')} | EMA200: {h1_ind.get('ema200')}
  RSI: {h1_ind.get('rsi')} | ADX: {h1_ind.get('adx')}
  Trend: {h1_ind.get('trend_bias')} | ATR: {h1_ind.get('atr')}

M15 CONTEXT:
  RSI: {m15_ind.get('rsi')} | ATR: {m15_ind.get('atr')}
  MACD Hist: {m15_ind.get('macd_histogram')}
  BB %B: {m15_ind.get('bb_pct_b')}

UPCOMING NEWS:
{news_text}

MACRO SENTIMENT: {context.get('macro_sentiment', {}).get('bias', 'unknown').upper()}

Review the scoring engine's decision now."""
